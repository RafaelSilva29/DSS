    private void confirmButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmButtonActionPerformed
        // TODO add your handling code here:
        Error error = new Error();
        int type = 0;
        Boolean flag = false;
        Boolean put = false;
        
        String radioText = "";
        String NIB = NIBTextField.getText();
        String NIF = NIFTextField.getText();
        String name = NameTextField.getText();
        String phoneNr = PhoneNrTextField.getText();
        String SS = SSTextField.getText();
        String userAdr = UserAdressTextField.getText();
        String userMail = UserEmailTextField.getText();
        String userName = UserNameTextField.getText();

        if(!NIB.isEmpty() && !NIF.isEmpty() && !name.isEmpty() && !phoneNr.isEmpty() && !SS.isEmpty() && !userAdr.isEmpty() && !userMail.isEmpty() && !userName.isEmpty() && (EmployeeRadioButton.isSelected() || SellerRadioButton.isSelected())){
            if(EmployeeRadioButton.isSelected()){
                type = 2;
                flag = true;
            } else if(SellerRadioButton.isSelected()){
                type = 1;
                     flag = true;  
            } else {
               error.showError("Please select a profession!!");
               flag = false;
            }    
         } else {
            if(EmployeeRadioButton.isSelected() && SellerRadioButton.isSelected()){
                error.showError("You can only choose one type of user!!!");
                EmployeeRadioButton.setSelected(false);
                SellerRadioButton.setSelected(false);
            } else { 
                error.showError("Please complete all required text fields!!");
                flag = false;
            }
         } 
 
         if(flag){
            try{
                put = this.cf.addUser(userName,name,userMail,phoneNr,userAdr,NIB,NIF,SS,type); 
   
            } catch(Exception e){
                error.showError("Error in user creation!!");
            }
            
            if(put){
                Success success = new Success();
                success.showSuccess(this.user,"Admin");
                this.hide();
            } else {
                error.showError("Error in user creation!!");
            }   
         }  
    }//GEN-LAST:event_confirmButtonActionPerformed
   
