    private double setDefaultPrice(String model){
        double price = 0;
        if (model.equals("BMW_1"))                {price = 15000.00;}
            else if (model.equals("BMW_2"))       {price = 18000.00;}
            else if (model.equals("BMW_3"))       {price = 20000.00;}
            else if (model.equals("BMW_4"))       {price = 25000.00;}
            else if (model.equals("Mini_1"))      {price = 17000.00;}
            else if (model.equals("Mini_2"))      {price = 19000.00;}
            else if (model.equals("Mini_3"))      {price = 22000.00;}
            else if (model.equals("Mini_4"))      {price = 25000.00;}
            else if (model.equals("RollsRoyce_1")){price = 20000.00;}
            else if (model.equals("RollsRoyce_2")){price = 25000.00;}
            else if (model.equals("RollsRoyce_3")){price = 30000.00;}
            else if (model.equals("RollsRoyce_4")){price = 35000.00;}
        return price;
    }
    
