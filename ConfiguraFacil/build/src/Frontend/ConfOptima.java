    private void GenerateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GenerateButtonActionPerformed
        // TODO add your handling code here:
        String brand = BrandComboBox.getSelectedItem().toString();
        String model = ModelComboBox.getSelectedItem().toString();
        String budget = BudgetTextField.getText();
        double price = setDefaultPrice(model);
        
        if(!brand.isEmpty() && !model.isEmpty() && !budget.isEmpty()){  
            try{
                if(price < Double.valueOf(budget)){
                    double money = Double.valueOf(budget);
       
                    Car car = this.cf.generateCar(brand,model,money,price);

                    if(!car.getBrand().isEmpty()){
                        
                        Map<Integer,Component> comps = new HashMap<>();
                        comps.putAll(car.getComponets());

                        if(comps.size()>=7){
                            ViewCar viewcar = new ViewCar(); 
                            this.alert.setText("");
                            try {
                                viewcar.showViewCar(user, 0, "ConfOpt", car);
                                this.hide();
                            } catch (Exception ex) {
                                System.out.println("Error: "+ ex.getMessage());
                            }
                            
                        } else {
                            this.alert.setForeground(Color.red);
                            this.alert.setText("Insufficient founds!!");
                        }   
                    } else{
                        this.alert.setForeground(Color.red);
                        this.alert.setText("Error o create car!!");
                    }
                } else{
                     this.alert.setForeground(Color.red);
                     this.alert.setText("Insufficient founds!!");
                }
                
            } catch(Exception ex){
                System.out.println("Error: "+ex.getMessage());
                this.alert.setForeground(Color.red);
                this.alert.setText("Please insert a valid budget!!");    
            }   
        } else{
             this.alert.setForeground(Color.red);
             this.alert.setText("Insert data in all fields!!");
        }
       
        
        
    }//GEN-LAST:event_GenerateButtonActionPerformed

