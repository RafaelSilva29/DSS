    private void loginExec() throws Exception{
      
       String username = this.usernamefield.getText();
       String pass = this.passwordfield.getText();
       
       int num = 0;
       
       num = this.cf.login(username, pass);

       switch(num){
           case(0): this.alert.setText("Username/Password\n incorrect");
                    this.usernamefield.setText("");
                    this.passwordfield.setText("");
                    break;
           case(1): this.alert.setText("Login sucess !"); 
                    MenuSeller mSeller = new MenuSeller();
                    mSeller.showMenuSeller(username);
                    this.hide();
                    break;
           case(2): this.alert.setText("Login sucess !");
                    MenuEmployee mEmployee = new MenuEmployee();
                    mEmployee.showMenuEmployee(username);
                    this.hide();
                    break;
           case(3): this.alert.setText("Login sucess !");
                    MenuAdmin menuadmin = new MenuAdmin();
                    menuadmin.showMenuAdmin(username);
                    this.hide();
                    break;     
       }
       
       // 0 -> nao da
       // 1 -> vendedor
       // 2 -> funcionario
       // 3 -> admin
    }
    
