    private void RemoveComponentButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RemoveComponentButtonActionPerformed
         // TODO add your handling code here:
        this.cf = new ConfiguraFacil();
        Error error = new Error();
       
       try{
           int row = this.jTable1.getSelectedRow();
           int idComponent = Integer.valueOf(jTable1.getModel().getValueAt(row, 0).toString());
           boolean flag = this.cf.removeComponent(idComponent);
           
           if(flag == true){
               this.hide();
               showMenuComponent(this.user);
           } else {
               error.showError("Unable to remove Component!!");
           }
  
       } catch(Exception e){
          error.showError("Please select a Component to Remove!!");
       }     
    }//GEN-LAST:event_RemoveComponentButtonActionPerformed

