    private void fillTableComponents() throws Exception{
        this.cf = new ConfiguraFacil();   
        List<Component> components = new ArrayList<Component>();
        
        DefaultTableModel componentable = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row, int column) {
                return column==4;
            }
        };  
        
        this.jTable1.setModel(componentable);
        componentable.addColumn("ComponentID");
        componentable.addColumn("Name");
        componentable.addColumn("Description");
        componentable.addColumn("Price");
        componentable.addColumn("Compatibility");
        
        components.addAll(this.cf.getAllComponents());
        
        for(Component c : components){
            componentable.addRow(new Object[]{c.getID(),c.getName(),c.getDescription(),c.getPrice(),c.getCompatibility()});
        }
    }
    
    public void showMenuComponent(String user) throws Exception{
        fillTableComponents();
        this.user = user;
        this.setLocationRelativeTo(null); //center form in the screen
        this.setVisible(true);
        this.pack();
        this.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE); // deve s√≥ fechar a pagina....
    }
    
