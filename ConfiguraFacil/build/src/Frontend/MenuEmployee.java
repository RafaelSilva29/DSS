    private void updatebuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatebuttonActionPerformed
        // TODO add your handling code here:
      
        Boolean done;
        Error erro = new Error();
        
        try{
            int row = ComponentTable.getSelectedRow();
            int column =0;
            int id =  (Integer) ComponentTable.getModel().getValueAt(row, column);
            int quantity = (Integer) qntGet.getValue();
            try{
                done = cf.updateStock(id,quantity);
                if (done == false){ 
                    erro.showError("Nonexistent ID!");
                }
                else{
                    Success success = new Success();
                    success.showSuccess(this.username, "Employee");
                }
            }catch(Exception c){
                Error e = new Error();
                e.show();
            }   
        } catch(Exception e){ 
            erro.showError("Pleade Select a Component to Update");
        }
        
        
    }//GEN-LAST:event_updatebuttonActionPerformed

