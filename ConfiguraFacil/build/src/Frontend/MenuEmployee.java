    public void showMenuEmployee(String username) throws Exception{
        this.userField.setText(username);
        this.username = username;
        fillTableComponents();
        fillTableOrdes();
        this.setLocationRelativeTo(null); //center form in the screen
        this.setVisible(true);
        this.pack();
        this.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
    }
    
    private void fillTableOrdes() throws Exception{
        this.cf = new ConfiguraFacil();   
        List<Order> orders = new ArrayList<Order>();

        DefaultTableModel ordertable = new DefaultTableModel(){     
        @Override
            public boolean isCellEditable(int row, int column) {
                return column==3;
            }
        };  
        
        orderTable.setModel(ordertable);
        ordertable.addColumn("Date");
        ordertable.addColumn("Status");
        ordertable.addColumn("CarID");
        
        orders.addAll(this.cf.getAllOrders());
        
        for(Order o : orders){
            ordertable.addRow(new Object[]{o.getDate(),o.getStatus(),o.getCarID()});
        }
    }
    
     private void fillTableComponents() throws Exception{
        this.cf = new ConfiguraFacil();   
        List<Component> components = new ArrayList<Component>();
        
        DefaultTableModel componentable = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row, int column) {
                return column==3;
            }
        };  
        
        this.ComponentTable.setModel(componentable);
        componentable.addColumn("ComponentID");
        componentable.addColumn("Name");
        componentable.addColumn("Quantity");
        
        components.addAll(this.cf.getAllComponents());
        
        for(Component c : components){
            componentable.addRow(new Object[]{c.getID(),c.getName(),c.getQuantity()});
        }
    }
     
