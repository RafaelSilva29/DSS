    private void fillTablePackages() throws Exception{
        this.cf = new ConfiguraFacil();   
        List<Package> packages = new ArrayList<Package>();
       
        DefaultTableModel packagetable = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row, int column) {
                return column==5;
            }
        };  
        
        this.jTable1.setModel(packagetable);
        packagetable.addColumn("PackageID");
        packagetable.addColumn("Name");
        packagetable.addColumn("Description");
        packagetable.addColumn("Price");
        packagetable.addColumn("Compatibility");
        
        packages.addAll(this.cf.getAllPackages());
        
        for(Package p : packages){
            packagetable.addRow(new Object[]{p.getPackageID(),p.getName(),p.getDescription(),p.getPrice(),p.getCompatibility()});
        }       
    }
     
     public void showMenuPackage(String user) throws Exception{
        this.user = user;
        fillTablePackages();
        this.setLocationRelativeTo(null); //center form in the screen
        this.setVisible(true);
        this.pack();
        this.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE); // deve s√≥ fechar a pagina....
    }

