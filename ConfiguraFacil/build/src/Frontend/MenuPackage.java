    private void RemovePackageButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RemovePackageButtonActionPerformed
       this.cf = new ConfiguraFacil();
       Error error = new Error();
       
       try{
           int row = this.jTable1.getSelectedRow();
           int idPackage = Integer.valueOf(jTable1.getModel().getValueAt(row, 0).toString());
           boolean flag = this.cf.removePackage(idPackage);
           
           if(flag == true){
               this.hide();
               showMenuPackage(this.user);
           } else {
               error.showError("Unable to remove Component!!");
           }
  
       } catch(Exception e){
           error.showError("Please select a Package to Remove!!");
       }     
    }//GEN-LAST:event_RemovePackageButtonActionPerformed

