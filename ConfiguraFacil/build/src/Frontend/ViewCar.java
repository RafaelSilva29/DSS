   public void fillContentViewCar(int idCar, Car car) throws Exception{
       this.cf = new ConfiguraFacil();
       
       if(this.path.equals("MenuSeller")){
            car = this.cf.getCarByID(idCar);
       } 
       
        this.brandField.setText(car.getBrand());
        this.modelField.setText(car.getModel());
        this.TotalPriceField.setText(String.valueOf(car.getTotalPrice()));
        
        DefaultTableModel componentstable = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row, int column) {
                return column==3;
            }
        };
        
        this.componentsTable.setModel(componentstable);
        componentstable.addColumn("Name");
        componentstable.addColumn("Description");
        componentstable.addColumn("Price");
        
        for(Component c : car.getComponets().values()){
            componentstable.addRow(new Object[]{c.getName(),c.getDescription(),c.getPrice()});
        }
        
        DefaultTableModel packagestable = new DefaultTableModel(){
            @Override
            public boolean isCellEditable(int row, int column) {
                return column==3;
            }
        };
        
        this.packagesTable.setModel(packagestable);
        packagestable.addColumn("Name");
        packagestable.addColumn("Description");
        packagestable.addColumn("Price");
        
        for(Package p : car.getPackages().values()){
            if (p.getName().equals("SportPack")){
                this.SportCheckBox.setSelected(true);
            }
            else if (p.getName().equals("EconomicPack")){
                this.EconomicCheckBox.setSelected(true);
            }
            else if (p.getName().equals("ComfortPack")){
                this.ConfortCheckBox.setSelected(true);
            }
            else{
            packagestable.addRow(new Object[]{p.getName(),p.getDescription(),p.getPrice()});}
        }
   }
    
