   public void showViewCar(String username, int idCar, String path, Car car) throws Exception{
       this.username = username;
       this.idCar = idCar;
       this.path = path;
       this.car = car;
       initViewCar();
       fillContentViewCar(idCar, car);
       this.setLocationRelativeTo(null); //center form in the screen
       this.setVisible(true);
       this.pack();
       this.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
   }

